import SwiftUI

enum ColorTheme {
    static let softPink = Color(red: 1.0, green: 0.8, blue: 0.86)
    static let softBlue = Color(red: 0.8, green: 0.88, blue: 1.0)
    static let lightGray = Color(red: 0.9, green: 0.9, blue: 0.9)
    static let lightGreen = Color(red: 0.6, green: 0.9, blue: 0.6)
    static let deepBlue = Color(red: 0.1, green: 0.2, blue: 0.6)
    static let brightPink = Color(red: 1.0, green: 0.2, blue: 0.6)
    static let darkGray = Color(red: 0.2, green: 0.2, blue: 0.2)

}
